    def shift_rows(self,state):
        for r in range(1,4): state[r]=state[r][r:]+state[r][:r]
    def inv_shift_rows(self,state):
        for r in range(1,4): state[r]=state[r][-r:]+state[r][:-r]

    def encrypt_block(self,block):
        state=[[block[r+4*c] for c in range(4)] for r in range(4)]
        self.add_round_key(state,0)
        for rnd in range(1,self.Nr):
            self.sub_bytes(state)
            self.shift_rows(state)
            self.mix_columns(state)
            self.add_round_key(state,rnd)
        self.sub_bytes(state)
        self.shift_rows(state)
        self.add_round_key(state,self.Nr)
        return [state[r][c] for c in range(4) for r in range(4)]

    def decrypt_block(self,block):
        state=[[block[r+4*c] for c in range(4)] for r in range(4)]
        self.add_round_key(state,self.Nr)
        for rnd in range(self.Nr-1,0,-1):
            self.inv_shift_rows(state)
            self.inv_sub_bytes(state)
            self.add_round_key(state,rnd)
            self.inv_mix_columns(state)
        self.inv_shift_rows(state)
        self.inv_sub_bytes(state)
        self.add_round_key(state,0)
        return [state[r][c] for c in range(4) for r in range(4)]

    def _pad(self,plaintext):
        padding_len = 16 - (len(plaintext) % 16)
        padding = [padding_len] * padding_len
        return plaintext + bytes(padding)

    def _unpad(self,padded_text):
        padding_len = padded_text[-1]
        if padding_len < 1 or padding_len > 16:
            # Geçersiz doldurma, orijinal metni döndürmeyi dene
            return padded_text
        return padded_text[:-padding_len]

    def xtime(self,a): return ((a<<1)^0x1B)&0xFF if (a&0x80) else (a<<1)&0xFF
    def mix_single_column(self,a):
        a0, a1, a2, a3 = a[0], a[1], a[2], a[3]
        t = a0 ^ a1 ^ a2 ^ a3
        a[0] = (a0 ^ t ^ self.xtime(a0 ^ a1)) & 0xFF
        a[1] = (a1 ^ t ^ self.xtime(a1 ^ a2)) & 0xFF
        a[2] = (a2 ^ t ^ self.xtime(a2 ^ a3)) & 0xFF
        a[3] = (a3 ^ t ^ self.xtime(a3 ^ a0)) & 0xFF

    def mix_columns(self,state):
        for c in range(4):
            col=[state[r][c] for r in range(4)]
            self.mix_single_column(col)
            for r in range(4): state[r][c]=col[r]

    def inv_mix_columns(self,state):
        for c in range(4):
            s=[state[r][c] for r in range(4)]
